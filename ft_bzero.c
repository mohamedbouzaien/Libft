#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;

	i = 0;
	while ((size_t)i < n && *(char *)s != '\0')
	{
		*(char *)s = '\0';
		i++;
		s = s + 1;
	}
	s = s - n;
}
