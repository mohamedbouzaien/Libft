#include "libft.h"

int		ft_intlen(int n)
{
	int len;

	len = 0;
	if (n == -2147483648)
		return (11);
	if (n < 0)
	{
		len = 1;
		n = -n;
	}
	while (n > 0)
	{
		len++;
		n /= 10;
	}
	return (len);
}
